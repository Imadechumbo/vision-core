// ── Merge into per-module JSON files ──────────────────────────────────────────

const byModule = {};
for (const c of COMPLETIONS) {
  // Extract module from id: SF-01-002 → SF-01, SF-INT-001 → SF-INT
  const m = c.id.match(/^(SF-(?:\d{2}|[A-Z]+))-\d{3}$/);
  if (!m) { console.error('Bad ID:', c.id); continue; }
  (byModule[m[1]] ??= []).push(c);
}

let totalMerged = 0;

for (const [mod, completions] of Object.entries(byModule)) {
  const file = join(DIR, `${mod}.json`);
  const data = JSON.parse(readFileSync(file, 'utf8'));

  // Index existing specs by id
  const existing = new Map(data.specs.map(s => [s.id, s]));

  // Merge completions (add if not present)
  for (const c of completions) {
    if (!existing.has(c.id)) {
      data.specs.push({
        id:              c.id,
        module:          mod,
        module_name:     data.module_name,
        type:            c.type ?? null,
        title:           c.title,
        pre:             c.pre ?? null,
        input:           c.input ?? null,
        expected_output: c.expected_output ?? null,
        pass_criteria:   c.pass_criteria ?? null,
        fail_criteria:   c.fail_criteria ?? null,
        tags:            c.tags ?? [],
      });
      totalMerged++;
    }
  }

  // Sort by id
  data.specs.sort((a, b) => a.id.localeCompare(b.id));
  data.parsed_count       = data.specs.length;
  data.needs_review_count = 0;

  writeFileSync(file, JSON.stringify(data, null, 2), 'utf8');
  console.log(`  ${mod}.json  →  ${data.specs.length} specs`);
}

// ── Rebuild _needs-review.json (should be empty now) ─────────────────────────

writeFileSync(
  join(DIR, '_needs-review.json'),
  JSON.stringify({ total: 0, ids: [], specs: [] }, null, 2),
  'utf8',
);

console.log(`\n✅ Merged ${totalMerged} completions. _needs-review.json cleared.`);
