func TestBuildPRPlanSanitizesBranchAndBody(t *testing.T) {
	plan := BuildPRPlan(PRPlanInput{MissionID: " mission/../bad ~^:?*[\\ id ", ChangedFiles: []string{"a.go"}, IssueType: "cors", SuggestedStrategy: "patch", PatchSummary: "updated CORS", GateSnapshot: goldGates()})
	if !IsSafeBranchName(plan.WorkBranch) {
		t.Fatalf("unsafe branch: %q", plan.WorkBranch)
	}
	for _, want := range []string{"Mission ID:", "PASS GOLD: true", "PASS SECURE: true", "a.go", "Generated by Vision Aegis Core"} {
		if !strings.Contains(plan.Body, want) {
			t.Fatalf("body missing %q:\n%s", want, plan.Body)
		}
	}
}

