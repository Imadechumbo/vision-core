func TestEnsureCleanWorkingTree(t *testing.T) {
	dir := initRepo(t)
	if err := EnsureCleanWorkingTree(dir); err != nil {
		t.Fatalf("expected clean tree: %v", err)
	}
	if err := os.WriteFile(filepath.Join(dir, "changed.txt"), []byte("x"), 0644); err != nil {
		t.Fatal(err)
	}
	if err := EnsureCleanWorkingTree(dir); err == nil {
		t.Fatal("expected dirty tree")
	}
	if err := EnsureWorkingTreeOnlyHas(dir, []string{"changed.txt"}); err != nil {
		t.Fatalf("expected planned dirty tree accepted: %v", err)
	}
}

func TestMockPRClientDeterministicURLAndDryRun(t *testing.T) {
	mock := &MockPRClient{}
	resp, err := mock.OpenPR(context.Background(), OpenPRRequest{Owner: "acme", Repo: "vision"})
	if err != nil || resp.URL != "https://github.com/acme/vision/pull/1" {
		t.Fatalf("unexpected mock response: %#v %v", resp, err)
	}
	plan := BuildPRPlan(PRPlanInput{MissionID: "m", ChangedFiles: []string{"a.go"}, IssueType: "x", GateSnapshot: goldGates()})
	res := OpenPRDryRunAware(context.Background(), OpenFlowInput{DryRun: true, Plan: plan, Client: mock})
	if !res.OK || res.PROpened || mock.OpenCalls != 1 {
		t.Fatalf("dry-run should not call client/open PR: res=%#v calls=%d", res, mock.OpenCalls)
	}
}

