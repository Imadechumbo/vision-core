func goldGates() GateSnapshot {
	return GateSnapshot{PassGold: true, PassSecure: true, DeployAllowed: true, PromotionAllowed: true, SecurityBlockingTotal: 0, RollbackReady: true, ValidatorOK: true, PatcherOK: true}
}

func TestBuildPRPlanCanOpenWithGoldGates(t *testing.T) {
	plan := BuildPRPlan(PRPlanInput{MissionID: "mission_123", ChangedFiles: []string{"a.go"}, IssueType: "cors", GateSnapshot: goldGates()})
	if !plan.CanOpenPR {
		t.Fatalf("expected can open, blocked: %s", plan.BlockReason)
	}
	if plan.BaseBranch != DefaultBaseBranch {
		t.Fatalf("default base branch mismatch: %s", plan.BaseBranch)
	}
	if plan.WorkBranch != "vision/remediation/mission_123" {
		t.Fatalf("work branch mismatch: %s", plan.WorkBranch)
	}
}

func TestBuildPRPlanBlocksFailedGates(t *testing.T) {
	tests := []struct {
		name   string
		mutate func(*GateSnapshot)
		want   string
	}{
		{"pass gold", func(g *GateSnapshot) { g.PassGold = false }, "pass_gold"},
		{"pass secure", func(g *GateSnapshot) { g.PassSecure = false }, "pass_secure"},
		{"blocking", func(g *GateSnapshot) { g.SecurityBlockingTotal = 1 }, "security_blocking_total"},
		{"rollback", func(g *GateSnapshot) { g.RollbackReady = false }, "rollback_ready"},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			g := goldGates()
			tt.mutate(&g)
			plan := BuildPRPlan(PRPlanInput{MissionID: "m", ChangedFiles: []string{"a.go"}, IssueType: "x", GateSnapshot: g})
			if plan.CanOpenPR || !strings.Contains(plan.BlockReason, tt.want) {
				t.Fatalf("expected block containing %q, got can=%v reason=%q", tt.want, plan.CanOpenPR, plan.BlockReason)
			}
		})
	}
}

