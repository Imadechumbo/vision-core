func TestBuildPassGoldStatus(t *testing.T) {
	if s := BuildPassGoldStatus(goldGates()); s.State != "success" || s.Context != DefaultStatusContext {
		t.Fatalf("expected success status, got %#v", s)
	}
	g := goldGates()
	g.PassGold = false
	if s := BuildPassGoldStatus(g); s.State != "failure" {
		t.Fatalf("expected failure for passgold")
	}
	g = goldGates()
	g.PassSecure = false
	if s := BuildPassGoldStatus(g); s.State != "failure" {
		t.Fatalf("expected failure for passsecure")
	}
	g = goldGates()
	g.SecurityBlockingTotal = 1
	if s := BuildPassGoldStatus(g); s.State != "failure" {
		t.Fatalf("expected failure for blocking")
	}
}

func initRepo(t *testing.T) string {
	t.Helper()
	dir := t.TempDir()
	runGit(t, dir, "init")
	runGit(t, dir, "config", "user.email", "vision@example.invalid")
	runGit(t, dir, "config", "user.name", "Vision Test")
	if err := os.WriteFile(filepath.Join(dir, "README.md"), []byte("init\n"), 0644); err != nil {
		t.Fatal(err)
	}
	runGit(t, dir, "add", "README.md")
	runGit(t, dir, "commit", "-m", "init")
	return dir
}

func runGit(t *testing.T, dir string, args ...string) {
	t.Helper()
	cmd := exec.Command("git", args...)
	cmd.Dir = dir
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("git %v failed: %v\n%s", args, err, out)
	}
}
